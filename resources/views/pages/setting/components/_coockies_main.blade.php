        <ul>
            <li><b>Necessary cookies.</b> These are cookies that are required for the operation of our Site and App. They include, for example, cookies that enable you to log into secure areas of our Site and App.</li>
            <li><b>Functionality cookies.</b> These are required for our Site or App to function correctly and to give you access to Cookpad Service that we offer. For example, these cookies allow the Site to remember any settings you make and provide enhanced and more personal features.</li>
            <li><b>Performance cookies.</b> These allow us to recognise and count the number of visitors and to see how visitors move around our Site or App when they are using it. This helps us to improve the way our Site and App work, for example, by ensuring that users are finding what they are looking for easily. </li>
            <li><b>Targeting or Advertising cookies</b>. These cookies record your visit to our Site and App, the pages you have visited and the links you have followed. We will use this information to make our Site and App and the advertising displayed on it more relevant to your interests. We may also share this information with third parties for this purpose.</li>
        </ul>

