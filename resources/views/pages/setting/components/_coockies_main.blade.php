        <h2>What is our cookie policy?</h2>

        <p>When you browse the Site or App, we typically receive some information automatically from your browser. This is done in the form of cookies, small files that we send to your browser which are then stored on your device. We may also use similar techniques with our Site and App which shall also be referred to as ‘cookies’ in this policy.</p>

