        <h2 id="how-to">How to</h2>

        <h3 id="how-do-i-adddelete-a-recipe">How do I add/delete a recipe?</h3>

        <p>To add a recipe:</p>

        <ol>
            <li>If you’re on app, click the green “Write a recipe” button.</li>
            <li>If you’re on web, click the green “Create recipe” button.</li>
        </ol>

        <p>To delete a recipe:</p>

        <ol>
            <li>If you’re on app, scroll to the bottom of the recipe, click the horizontal ellipsis (three dot icon) on the lower left of the screen, and click “Delete this recipe.”</li>
            <li>If you’re on web, click the horizontal ellipsis (three dot icon) on the lower right of the screen, and then click “Delete.”</li>
        </ol>

        <h3 id="how-do-i-search-for-a-recipe">How do I search for a recipe?</h3>

        <p>Enter your search terms in the search box (which has the search icon and “ingredient name, dish” in light gray text) and hit return.</p>

        <h3 id="how-can-i-communicate-with-the-other-authors">How can I communicate with the other authors?</h3>

        <p>If you’re in app, you can start a one-on-one chat (there’s a chat icon on your upper right screen) with any fellow user whom you are following. There is also an option for group chat where you can add up to 12 participants.</p>

        <p>If you’re on web, you can scroll to the bottom of a recipe and make a comment to the recipe author in the comment box at the VERY end of the recipe (You’ll see a light gray text prompt, “Comment on [recipe name].” )</p>

        <h3 id="how-do-i-follow-an-author">How do I follow an author?</h3>

        <p>Whenever you see a recipe, next to the author’s name is button that allows you to follow or unfollow. If it says “Follow” that means you’re not following them yet, and you just have to click the button to follow them, then the button will say “Following.”</p>

        <p>To unfollow, you simply click the button that says “Following” and when you see the text change to “Follow” then you know you are no longer following them.</p>

        <h3 id="how-do-i-adddelete-a-bookmark">How do I add/delete a bookmark?</h3>

        <p>To add a bookmark, click the bookmark icon. When it turns orange, you’ll know it’s been bookmarked.</p>

        <p>To delete a bookmark, click the same icon and when it turns white again, you’ll know the bookmark has been deleted.</p>

        <h3 id="how-can-i-download-cookpad-app">How can I download cookpad app?</h3>

        <p>Go to the App Store or Google Play and search for ‘Cookpad’ and click get/download. It is a free app.</p>

        <h3 id="how-can-i-keep-a-recipe-private">How can I keep a recipe private?</h3>

        <p>If you’d like to keep a recipe private, all you have to do is enter the recipe as usual, and then:</p>

        <ol>
            <li>If you’re on web, don’t click Publish. The recipe will save as you go, and it will remain in your recipes for your records with a lock icon next to it.</li>
            <li>If you’re on app, just click Save without clicking Publish, and the recipe will remain in your recipes for your records with a lock icon next to it.</li>
        </ol>

        <p>You can access and edit these unpublished recipes at any time, and they will be private.</p>

        <h3 id="how-can-i-see-my-recipes-statistics">How can I see my recipes’ statistics?</h3>

        <p>Click on the menu and you will see what looks like a graph. If you click on this, you will be taken to your stats page.</p>

        <h3 id="how-can-i-make-corrections-to-an-already-published-recipe">How can I make corrections to an already published recipe?</h3>

        <p>To make corrections to an already published recipe:</p>

        <ol>
            <li>If you’re on web, click into the recipe, click your cursor to the place you’d like to begin editing, and just make your edits. Your changes will save automatically, and there’s nothing else you have to do.</li>
            <li>If you’re on app, click into the recipe, click your cursor to the place you’d like to being editing, make your edits, and when done, click Save.</li>
        </ol>

        <h3 id="how-can-i-add-a-photo-to-my-published-recipe">How can I add a photo to my published recipe?</h3>

        <p>At any time, you can click into any step in a recipe, click on the camera icon, and add a photo.</p>

        <h3 id="how-can-i-comment-on-a-recipe-app-users">How can I comment on a recipe (App users)?</h3>

        <p>If you have questions for the recipe author and are following them, you can also ask them in chat. (Look for chat icon on upper left of screen.)</p>

        <h3 id="i-have-2-account-on-cookpad-how-can-i-merge-them">I have 2 account on Cookpad, how can I merge them?</h3>

        <p>Sadly, we are unable to merge accounts but you can copy all of the recipes over to just one account and request to remove the other account.</p>
